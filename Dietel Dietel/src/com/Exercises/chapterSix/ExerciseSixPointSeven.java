package com.Exercises.chapterSix;

public class ExerciseSixPointSeven {

    public static void main(String[] args) {

        System.out.println("x is " +Math.abs(7.5));
        System.out.println("x is " +Math.floor(7.5));
        System.out.println("x is " +Math.abs(0.0));
        System.out.println("x is " +Math.ceil(0.0));
        System.out.println("x is " +Math.abs(-6.4));
        System.out.println("x is " +Math.ceil(-6.4));
        System.out.println("x is " +Math.ceil(-Math.abs(-8 + Math.floor(-5.5))));
    }
}
